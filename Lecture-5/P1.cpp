#include <iostream>
using namespace std;

int main(){
    // Bitwise Operators 
    // AND -> &
    // OR  -> |
    // NOT -> ~
    // XOR -> ^
    int a, b;
    cout << "Enter first num: ";
    cin >> a;
    cout << "Enter second num: ";
    cin >> b;
    cout << "AND" << endl;
    cout << a & b;
}